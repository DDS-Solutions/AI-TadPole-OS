import { describe, it, expect, vi, beforeEach, afterEach } from 'vitest';
import { use_sovereign_store } from './sovereign_store';

// Mock Broadcast Channel API for test environment
class MockBroadcastChannel {
    name: string;
    onmessage: ((ev: { data: any }) => void) | null = null;
    messages: any[] = [];
    
    constructor(name: string) {
        this.name = name;
    }

    postMessage(data: any) {
        this.messages.push(data);
    }
    
    close() {}
}

const mock_channel = new MockBroadcastChannel('test-channel');

vi.stubGlobal('BroadcastChannel', vi.fn().mockImplementation(() => mock_channel));

describe('use_sovereign_store', () => {
    beforeEach(() => {
        // Reset state
        use_sovereign_store.setState({
            messages: [],
            message_index_by_id: {},
            active_scope: 'agent',
            selected_agent_id: null,
            target_agent: 'Agent of Nine',
            target_cluster: '',
            is_detached: false,
        });
        mock_channel.messages = [];
        
        // Mock UUID if not in high quality environment
        vi.stubGlobal('crypto', {
             randomUUID: () => 'mock-uuid-1234'
        });
    });

    afterEach(() => {
        vi.unstubAllGlobals();
    });

    it('has the correct default state', () => {
        const state = use_sovereign_store.getState();
        expect(state.messages).toEqual([]);
        expect(state.active_scope).toBe('agent');
        expect(state.target_agent).toBe('Agent of Nine');
        expect(state.is_detached).toBe(false);
    });

    describe('add_message', () => {
        it('adds a new message with autogenerated id and timestamp', () => {
            const store = use_sovereign_store.getState();
            store.add_message({
                sender_id: 'sys',
                sender_name: 'System',
                text: 'Hello Team',
                scope: 'agent'
            });

            const state = use_sovereign_store.getState();
            expect(state.messages).toHaveLength(1);
            expect(state.messages[0].text).toBe('Hello Team');
            expect(state.messages[0].id).toBe('mock-uuid-1234');
            expect(state.messages[0].timestamp).toBeDefined();
        });

        it('deduplicates messages with the same id', () => {
            const store = use_sovereign_store.getState();
            const msg = {
                id: 'fixed-123',
                sender_id: 'sys',
                sender_name: 'System',
                text: 'Hello Team',
                scope: 'agent' as const
            };

            store.add_message(msg);
            store.add_message(msg); // Attempt to add same ID again

            const state = use_sovereign_store.getState();
            expect(state.messages).toHaveLength(1);
        });
    });

    describe('Setters', () => {
        it('sets active scope', () => {
            const store = use_sovereign_store.getState();
            store.set_scope('cluster');
            expect(use_sovereign_store.getState().active_scope).toBe('cluster');
        });

        it('sets selected agent id', () => {
            const store = use_sovereign_store.getState();
            store.set_selected_agent_id('agent-id-99');
            expect(use_sovereign_store.getState().selected_agent_id).toBe('agent-id-99');
        });

        it('sets target agent', () => {
            const store = use_sovereign_store.getState();
            store.set_target_agent('DevOps Bot');
            expect(use_sovereign_store.getState().target_agent).toBe('DevOps Bot');
        });

        it('sets target cluster', () => {
            const store = use_sovereign_store.getState();
            store.set_target_cluster('Engineering_Alpha');
            expect(use_sovereign_store.getState().target_cluster).toBe('Engineering_Alpha');
        });

        it('sets is_detached', () => {
            const store = use_sovereign_store.getState();
            store.set_detached(true);
            expect(use_sovereign_store.getState().is_detached).toBe(true);
        });
    });

    describe('clear_history', () => {
        it('clears all messages from the store', () => {
            const store = use_sovereign_store.getState();
            store.add_message({
                sender_id: 'sys',
                sender_name: 'System',
                text: 'Sweep me',
                scope: 'agent'
            });

            expect(use_sovereign_store.getState().messages).toHaveLength(1);
            
            store.clear_history();
            expect(use_sovereign_store.getState().messages).toHaveLength(0);
        });
    });
});

